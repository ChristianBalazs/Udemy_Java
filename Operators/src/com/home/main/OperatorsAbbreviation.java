package com.home.main;

public class OperatorsAbbreviation {

	public static void main(String[] args) {
	
		int result = 1;
		//Abbreviation for increment by 1
		// result = result + 1
		result++; // 1 + 1 = 2
		System.out.println("1 + 1 = " + result);
				
				
		//Abbreviation for subtraction by 1
		// result = result + 1
		result--; //2 - 1 = 1
		System.out.println("2 - 1 = " + result);
				
				
		//Abbreviation for increment by 2
		// result = result + 2
		result += 2;  // 1 + 2 = 3
		System.out.println("1 + 2 = " + result);
				

				
				
		// Abbreviation for multiply by 10
		// result = result * 10
		result *= 10;  //3 * 10 = 30
		System.out.println("3 * 10 = " + result);
				
				
		//Abbreviation for divide by 3
		// result = result /3 ;
		result /= 3;  // 30 / 3 = 10 
		System.out.println("30 / 3 = " + result);
				
				
		//Abbreviation for subtraction by 2
		// result = result - 2
		result-= 2; //10 - 2 = 8
		System.out.println("10 - 2 = " + result);
				

	}

}
