package com.home.main;

public class OtherPrimitive {

	public static void main(String[] args) {
		
		
		// byte
		// A byte occupies 8 bits
		
		byte myMinByteValue = Byte.MIN_VALUE;
		byte myMaxByteValue = Byte.MAX_VALUE;
		
		System.out.println("Byte Min Value = " + myMinByteValue);
		System.out.println("Byte Max Value = " + myMaxByteValue);
		
		
		// short
		// A short occupies 16 bits
		short myMinShortValue = Short.MIN_VALUE;
		short myMaxShortValue = Short.MAX_VALUE;
		
		System.out.println("Short Min Value = " + myMinShortValue);
		System.out.println("Short Max Value = " + myMaxShortValue);
		
		
		// int 
		// A int occupies 32 bits
		int myMinIntValue = Integer.MIN_VALUE;		
		int myMaxIntValue = Integer.MAX_VALUE;
		
		System.out.println("Integer Min value = " + myMinIntValue);
		System.out.println("Integer Max value = " + myMaxIntValue);
		
		
		//long
		// A long occupies 64 bits
		
		// When declaring a long variable 
		long myLongValue = 100L; // need to place letter L at the end 
		
		long myMinLongValue = Long.MIN_VALUE;
		long myMaxLongValue = Long.MAX_VALUE;
		
		System.out.println("Long Min Value = " + myMinLongValue);
		System.out.println("Long Max Value = " + myMaxLongValue);
							


	}

}
