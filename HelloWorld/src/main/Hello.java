package main;


//public and class are JAVA keywords
//public = access modifier
//class keyword used to define a class
//Hello is the name of the class
//between {} is the body of the class

public class Hello {

//method is a collection of statements that performs an operation
//main method is the method Java looks for when running a program

	
	// static = Java keyword
	// void = Java keyword to indicate the method doesn't return anything
	// parentheses () are needed when defining a method
	// main() is the main method
	// inside () can add method parameters 
	
	public static void main(String[] arg) {
		// added a statement inside the main method
		// a println command to print text "Hello World"
		System.out.println("Hello, World");
		
		// change the text inside the print command
		System.out.println("Hello, Chris!");
				
			
	}
	
	
}
